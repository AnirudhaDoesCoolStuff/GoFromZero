package main

import "fmt"

func main() {
	// Using Println
	fmt.Println("Hello World!")

	// Using Print Instead of Println
	fmt.Print("Hello, ")
	fmt.Print("World \n")

	// Using Printf for Formatting
	fmt.Printf("Hello, %s \n", "Go🚀")

	fmt.Println("こんにちは, 世界!") // Hello, World! in Japanese
	fmt.Println("😊 Hello, World!") // Using emoji	
}